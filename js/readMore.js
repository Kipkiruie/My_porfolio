function readMore1() {
    let dots = document.getElementById("dots1");
    let moreText = document.getElementById("more1");
    let btnText = document.getElementById("myBtn1");

    if (dots.style.display === "none") {
        dots.style.display = "inline";
        btnText.innerHTML = "Read more <i class=\"fas fa-arrow-right\"></i>";
        moreText.style.display = "none";
    } else {
        dots.style.display = "none";
        btnText.innerHTML = "Read less <i class=\"fas fa-arrow-right\"></i>";
        moreText.style.display = "inline";
    }
}

